#include <iostream>
using namespace std;

//C++ Program to Add/Subtract/Multiply/Divide Two Integers.
int main(){
    int a = 10;
    int b = 5;
    int add = a + b;
    int sub = a + b;
    int mult = a * b;
    int div = a / b;
    cout << "Adding two integers." << endl;
    cout << add << endl;
    cout << "Subtracting two integers." << endl;
    cout << sub << endl;
    cout << "Multiplying two integers." << endl;
    cout << mult << endl;
    cout << "Dividing two integers." << endl;
    cout << div << endl;
    return 0;
}